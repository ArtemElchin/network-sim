package com.alumisky.util.concurrent;

/**
 *
 *
 */
public class ConcurrentHelpers {

    /**
     * <p>
     * Does ...</p>
     *
     * @param millis
     */
    public static void sleep(long millis) {
        try {
            Thread.sleep(millis);
        } catch (InterruptedException e) {
            e.printStackTrace();
        }
    }
}
